#include "libft.h"

long	ft_atoi(const char *str)
{
	unsigned long		result;
	int					sign;
	int					i;

	result = 0;
	sign = 1;
	i = 0;
	while ((str[i] == 32) || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10 + (str[i] - '0');
		i++;
		if (result > 2147483647 && sign == 1)
			return (2147483648);
		if (result > 2147483648 && sign == -1)
			return (2147483648);
	}
	return (result * sign);
}
